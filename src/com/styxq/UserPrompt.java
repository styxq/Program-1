package com.styxq;

public class UserPrompt {
    private int a;
    private int b;
    private int c;

    public UserPrompt(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }



    public int getA() {
        return this.a;
    }

    public int getB() {
        return this.b;
    }

    public int getC() {
        return this.c;
    }
}
